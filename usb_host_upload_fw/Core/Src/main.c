/* ================= JUMP TO APP ================= */
void JumpToApp(void)
{
    uint32_t sp = *(uint32_t*)APP_ADDR;

    if (sp < 0x20000000 || sp > 0x20020000)
    {
        printf("Invalid firmware\n");
        return;
    }

    uint32_t resetHandlerAddr = *(uint32_t*)(APP_ADDR + 4);
    void (*resetHandler)(void) = (void*)resetHandlerAddr;

    printf("Jumping to application...\n");
    HAL_Delay(100);

    HAL_DeInit();
    __disable_irq();

    /* Clear all interrupts */
    for (int i = 0; i < 8; i++)
    {
        NVIC->ICER[i] = 0xFFFFFFFF;
        NVIC->ICPR[i] = 0xFFFFFFFF;
    }

    SCB->VTOR = APP_ADDR;
    __set_MSP(sp);
    __set_CONTROL(0);

    resetHandler();
}

/* ================= FLASH FROM USB ================= */
void Flash_From_USB(void)
{
    FATFS fs;
    FIL file;
    UINT br;
    uint8_t buffer[256];
    uint32_t addr = APP_ADDR;

    printf("\n--- USB Flash Start ---\n");

    if (f_mount(&fs, "", 1) != FR_OK)
    {
        printf("Mount failed\n");
        return;
    }

    if (f_open(&file, "firmware.bin", FA_READ) != FR_OK)
    {
        printf("File not found\n");
        return;
    }

    printf("File size: %lu bytes\n", f_size(&file));   // FIXED

    HAL_FLASH_Unlock();

    FLASH_EraseInitTypeDef erase;
    uint32_t error;

    erase.TypeErase = FLASH_TYPEERASE_SECTORS;
    erase.VoltageRange = FLASH_VOLTAGE_RANGE_3;
    erase.Sector = FLASH_SECTOR_4;
    erase.NbSectors = 4;

    if (HAL_FLASHEx_Erase(&erase, &error) != HAL_OK)
    {
        printf("Erase failed\n");
        HAL_FLASH_Lock();
        return;
    }

    printf("Erase done\n");

    while (1)
    {
        if (f_read(&file, buffer, 256, &br) != FR_OK || br == 0)
            break;

        while (br % 4 != 0)
            buffer[br++] = 0xFF;

        for (int i = 0; i < br; i += 4)
        {
            uint32_t data = *(uint32_t*)&buffer[i];

            if (HAL_FLASH_Program(FLASH_TYPEPROGRAM_WORD, addr, data) != HAL_OK)
            {
                printf("Write failed at 0x%08lX\n", addr);
                HAL_FLASH_Lock();
                return;
            }

            addr += 4;
        }
    }

    f_close(&file);
    HAL_FLASH_Lock();

    printf("Flash complete\n");

    HAL_Delay(1000);

    JumpToApp();
}

